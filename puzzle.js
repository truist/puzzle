"use strict";
var util = require('util');

var BOARD_SIZE = 56;
var baseData = [[21,18], [21,18], [14,4], [28,14], [28,7], [32,10], [10,7], [28,6], [17,14], [21,14], [21,14], [32,11]];

var counter = 0;
permute(baseData.length, baseData, function(data) {
    util.print(++counter + ": " + data + "\n");
    fillBoard(initBoard(), data, 0);
});

function fillBoard(board, data, dataIndex) {
    if (data.length == dataIndex) {
        printBoard(board);
        util.print("\n\nSUCCESS!!!\n\n");
        process.exit(0);
    }

    //printBoard(board);

    findCorners(board, function(x, y) {
        checkAndPlaceBothWays(board, data, dataIndex, x, y);
    });
}

function checkAndPlaceBothWays(board, data, dataIndex, x, y) {
    var piece = data[dataIndex];

    var newBoard = cloneBoard(board);
    if (checkAndPlace(newBoard, x, y, piece[0], piece[1], dataIndex + 1)) {
        fillBoard(newBoard, data, dataIndex + 1);
    }

    newBoard = cloneBoard(board);
    if (checkAndPlace(newBoard, x, y, piece[1], piece[0], dataIndex + 1)) {
        fillBoard(newBoard, data, dataIndex + 1);
    }
}

function cloneBoard(oldBoard) {
    var newBoard = [];
    var i = oldBoard.length;
    while (i--) {
        newBoard[i] = oldBoard[i].slice();
    }
    return newBoard;
}

function checkAndPlace(board, x, y, width, height, value) {
    if (x + width >= BOARD_SIZE || y + height >= BOARD_SIZE) return false;

    for (var i = x; i < x + width; i++) {
        for (var j = y; j < y + height; j++) {
            if (board[i][j]) {
                return false;
            } else {
                board[i][j] = value;
            }
        }
    }
    return true;
}

function permute(N, data, callback) {
    if (N === 1) {
        callback(data);
    } else {
        for (var c = 0; c < N; c++) {
            permute(N-1, data, callback);

            var swapIndex = N % 2 ? 1 : c;
            var temp = data[N-1];
            data[N-1] = data[swapIndex];
            data[swapIndex] = temp;
        }
    }
}

function findCorners(board, callback) {
    for (var y = BOARD_SIZE - 1; y >= 0; y--) {
        if (board[0][y]) {
            if (y < BOARD_SIZE - 1) {
                callback(0, y+1);
            }
            //we travel the un-filled space, watching the edges around us
            east(board, 0+1, y+1, callback);
            return;
        }
    }
    //special case, empty board
    callback(0, 0);
}

//our top edge must be against another piece
function east(board, x, y, callback) {
    if (BOARD_SIZE == x) return;

    if (!board[x][y-1]) {
        north(board, x, y-1, callback);
    } else if (board[x][y]) {
        south(board, x-1, y+1, callback);
    } else {
        east(board, x+1, y, callback);
    }
}

//our right edge must be against another piece
function south(board, x, y, callback) {
    if (BOARD_SIZE == y || !board[x+1][y]) {
        east(board, x+1, y, callback);
    } else {
        south(board, x, y+1, callback);
    }
}

//our left edge must be against another piece
function north(board, x, y, callback) {
    if (0 == y) {
        //we've made it to the top wall
        callback(x, y);
    } else if (!board[x-1][y]) {
        west(board, x-1, y, callback);
    } else if (board[x][y-1]) {
        //we've found a corner
        callback(x, y);
        east(board, x+1, y, callback);
    } else {
        north(board, x, y-1, callback);
    }
}

//our bottom edge must be against another piece
function west(board, x, y, callback) {
    if (board[x-1][y]) {
        north(board, x, y-1, callback);
    } else {
        west(board, x-1, y, callback);
    }
}

function printBoard(board) {
    for (var y = 0; y < BOARD_SIZE; y++) {
        for (var x = 0; x < BOARD_SIZE; x++) {
            if (x > 0 && x < BOARD_SIZE - 1 && y > 0 && y < BOARD_SIZE - 1 && 0 == board[x][y]) {
                util.print(" ");
            } else {
                util.print(board[x][y] % 10);
            }
            util.print(" ");
        }
        util.print("\n");
    }
    util.print("\n");
}

